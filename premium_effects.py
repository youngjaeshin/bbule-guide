def build_json():
    output = {}
    for name, main_list, sub_list in DATA:
        effects = []
        for abbr, val in main_list:
            full = expand(abbr)
            if val == '-':
                desc = full
            else:
                desc = f"{full} {val}"
            effects.append({
                "description": desc,
                "value_display": val if val != '-' else '',
                "is_sub": False,
            })
        for abbr, val in sub_list:
            full = expand(abbr)
            desc = f"{full} {val}"
            effects.append({
                "description": desc,
                "value_display": val,
                "is_sub": True,
            })
        output[name] = effects
    return output


