def expand(abbr):
    """Expand abbreviation to full name, preserving bracket prefixes."""
    # Handle [세트명] prefix or (조건) prefix
    if abbr.startswith('[') or abbr.startswith('('):
        # Find the closing bracket
        close = abbr.find(']') if abbr.startswith('[') else abbr.find(')')
        if close >= 0:
            prefix = abbr[:close + 1]
            rest = abbr[close + 1:].strip()
            full = ABBREV.get(rest, rest)
            return f"{prefix} {full}"
    return ABBREV.get(abbr, abbr)


